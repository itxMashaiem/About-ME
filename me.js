
function more() {
    var x = document.getElementById("firstcontent");
    if (x.style.display === "none") {
        x.style.display = "block";
        var y = document.getElementById("third-content");
        if (x.style.display = "block") {
            y.style.display = "none";
        }
        var z = document.getElementById("sec-content");
        if (x.style.display = "block") {
            z.style.display = "none";
        }
    }
    else {
        x.style.display = "none";
    }
}
